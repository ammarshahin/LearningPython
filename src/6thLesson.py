# ************* Tuples(Constant variables or Constant Lists)**************
# a tuple is a data structure that has a constant data value (Can't be changed)
# [tuple object doesn't support item assignment]

# create a tuple
PI = (3.1415)  # the value of this tuple(variable) can't be changed
print(PI)

# tuple list
coordinates = (4, 5)
print(coordinates[0], coordinates[1])
